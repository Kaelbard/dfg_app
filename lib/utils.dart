String formatPrice(String priceAsString) {
  num price = num.tryParse(priceAsString) ?? 0;
  String formattedPrice = formatNumber(
    price,
    formatType: FormatType.decimal,
    decimalType: DecimalType.commaDecimal,
    currency: 'R\$ ',
    decimalSeparator: ',',
    fractionDigits: 2,
  );
  if (price == price.truncate()) {
    formattedPrice += ',00';
  } else if (priceAsString.endsWith('.')) {
    formattedPrice += ',00';
  } else if (priceAsString.split('.')[1].length == 1) {
    formattedPrice += '0';
  }
  return formattedPrice;
}

